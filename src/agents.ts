import { join } from "node:path"

import type { AgentConfig, Config } from "@opencode-ai/sdk/v2"
import type { AgentName } from "./phases"

export function opencodeConfig(runDir: string): Config {
  const agent = {
    implementer: agentConfig(
      "Implementa la feature descrita en el PRD respetando los patrones del repo",
      0.2,
      implementerPrompt,
      runDir,
      false,
    ),
    "pattern-auditor": agentConfig(
      "Audita patrones y buenas practicas, aplica refactor sin cambiar el comportamiento",
      0.1,
      patternAuditorPrompt,
      runDir,
      false,
    ),
    "security-auditor": agentConfig(
      "Audita la implementacion nueva en busca de problemas de seguridad y los corrige",
      0.1,
      securityAuditorPrompt,
      runDir,
      false,
    ),
    "design-polisher": agentConfig(
      "Pule la UI nueva siguiendo el design system del repo, sin redisenar",
      0.2,
      designPolisherPrompt,
      runDir,
      false,
    ),
    "test-engineer": agentConfig("Asegura cobertura unit verde y disena flows Maestro para E2E", 0.2, testEngineerPrompt, runDir, false),
  } satisfies Record<AgentName, AgentConfig>

  return {
    agent,
    permission: {
      question: "deny",
    },
  }
}

function agentConfig(description: string, temperature: number, prompt: string, runDir: string, webfetch: boolean): AgentConfig {
  return {
    description,
    mode: "primary",
    temperature,
    tools: {
      read: true,
      write: true,
      edit: true,
      bash: true,
      webfetch,
    },
    permission: {
      edit: "allow",
      question: "deny",
      bash: {
        "*": "allow",
        "rm -rf /*": "deny",
        "rm -rf ~*": "deny",
        "git push *": "deny",
        "git reset --hard *": "deny",
        "gh *": "deny",
      },
      external_directory: {
        "*": "deny",
        [join(runDir, "**")]: "allow",
      },
    },
    prompt,
  }
}

